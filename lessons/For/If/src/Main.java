public class Main {
    public static void main(String[] args) {
        int value = 12;
        if (value < 10) {
            System.out.println("value < 10");
        } else if (value > 20) {
            System.out.println("value > 20");
        } else if (value == 15) {
            System.out.println("value == 15");
        } else {
            System.out.println("value != 15");
        }
    }
}